// Escribir un programa que solicite al usuario el número de kilómetros recorridos por su coche y el número de litros consumidos 

let kmRecorridos = parseFloat(prompt("Ingresa los kilómetros recorridos en el coche."));
let litrosConsumidos = parseFloat(prompt("Ingresa el número de gasolina consumida."));

alert(`Los kilómetros que recorriste son: ${kmRecorridos} Km y el consumo de gasolina fue de: ${litrosConsumidos} Litros`);